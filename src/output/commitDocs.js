import { commitDocsPR, commitDirectly } from '../github/prCreator.js';

/**
 * Commits generated documentation files to GitHub, either as a PR or directly to a branch.
 *
 * @param {string} owner
 * @param {string} repo
 * @param {Array<{filePath: string, content: string, prepend?: boolean}>} generatedFiles
 * @param {object} config
 * @param {object} prContext
 * @returns {Promise<{prUrl: string|null, filesCommitted: number, branch: string}>}
 */
export async function commitAndPR(owner, repo, generatedFiles, config, prContext = {}) {
  if (!generatedFiles || generatedFiles.length === 0) {
    console.log('[commitDocs] No files to commit');
    return { prUrl: null, filesCommitted: 0, branch: null };
  }

  const baseBranch = prContext.baseBranch || process.env.TARGET_DOCS_BRANCH || 'main';
  const openPR = config.open_pr !== false && process.env.OPEN_PR_FOR_DOCS !== 'false';

  // Handle CHANGELOG.md specially -- prepend new entry instead of replacing
  const processedFiles = await processChangelogFiles(owner, repo, generatedFiles, baseBranch);

  const prTitle = `docs: auto-generated documentation update${prContext.prNumber ? ` (PR #${prContext.prNumber})` : ''}`;
  const prBody = buildPRBody(generatedFiles, prContext);

  console.log(`[commitDocs] Committing ${processedFiles.length} files (open_pr: ${openPR})`);

  if (openPR) {
    try {
      const prUrl = await commitDocsPR(owner, repo, processedFiles, prTitle, prBody, baseBranch);
      return {
        prUrl,
        filesCommitted: processedFiles.length,
        branch: `docs/auto-update-${Date.now()}`,
      };
    } catch (err) {
      console.error('[commitDocs] Failed to create PR:', err.message);
      throw err;
    }
  } else {
    const docsBranch = process.env.TARGET_DOCS_BRANCH || 'docs/auto-generated';
    try {
      await commitDirectly(owner, repo, processedFiles, docsBranch, prTitle);
      return {
        prUrl: null,
        filesCommitted: processedFiles.length,
        branch: docsBranch,
      };
    } catch (err) {
      console.error('[commitDocs] Failed to commit directly:', err.message);
      throw err;
    }
  }
}

// Handles CHANGELOG.md specially: fetches existing content and prepends new entry.
async function processChangelogFiles(owner, repo, files, baseBranch) {
  const processed = [];

  for (const file of files) {
    if (file.filePath === 'CHANGELOG.md' && file.prepend) {
      let existingContent = '';
      try {
        const { getFileContent } = await import('../github/githubClient.js');
        existingContent = await getFileContent(owner, repo, 'CHANGELOG.md', baseBranch) || '';
      } catch {
        existingContent = '# Changelog\n\nAll notable changes to this project will be documented here.\n\n';
      }

      const titleMatch = existingContent.match(/^(#[^\n]*\n+(?:[^\n]*\n+)?)/);
      let newContent;
      if (titleMatch) {
        newContent = titleMatch[1] + file.content + existingContent.slice(titleMatch[1].length);
      } else {
        newContent = file.content + existingContent;
      }

      processed.push({ path: file.filePath, content: newContent });
    } else {
      processed.push({ path: file.filePath, content: file.content });
    }
  }

  return processed;
}

// Builds a descriptive PR body listing all generated files.
function buildPRBody(files, prContext) {
  const lines = [
    '## Auto-generated Documentation',
    '',
    'This PR was automatically generated by DocuBot.',
    '',
  ];

  if (prContext.prNumber) {
    lines.push(`**Triggered by:** PR #${prContext.prNumber} -- ${prContext.prTitle || ''}`, '');
  }

  lines.push('### Files Updated', '');
  for (const file of files) {
    lines.push(`- \`${file.filePath}\``);
  }

  lines.push('', '---', '_Do not edit these files manually -- they will be overwritten on the next PR merge._');

  return lines.join('\n');
}
