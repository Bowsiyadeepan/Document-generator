import path from 'path';

const AUTO_GENERATED_HEADER = (date) =>
  `<!-- AUTO-GENERATED by DocuBot on ${date} -- do not edit manually -->\n\n`;

/**
 * Converts a generator JSON result into a markdown file with path and content.
 *
 * @param {object} generatorResult - JSON output from any generator
 * @param {object} [context={}] - Additional context (prNumber, version, etc.)
 * @returns {{filePath: string, content: string}|null}
 */
export function convertToMarkdown(generatorResult, context = {}) {
  if (!generatorResult || !generatorResult.doc_type) return null;

  const date = new Date().toISOString().split('T')[0];
  const header = AUTO_GENERATED_HEADER(date);

  switch (generatorResult.doc_type) {
    case 'api_reference':
      return convertApiReference(generatorResult, header);

    case 'changelog_entry':
      return convertChangelog(generatorResult, header, context);

    case 'readme_update':
      return convertReadmeUpdate(generatorResult);

    case 'migration_guide':
      return convertMigrationGuide(generatorResult, header, context);

    case 'architecture_overview':
      return { filePath: 'docs/architecture.md', content: header + convertArchitecture(generatorResult) };

    case 'onboarding_guide':
      return { filePath: 'docs/getting-started.md', content: header + convertOnboarding(generatorResult) };

    case 'ops_runbook':
      return { filePath: 'docs/runbook.md', content: header + convertRunbook(generatorResult) };

    case 'inline_docstrings':
      return convertDocstrings(generatorResult);

    case 'stale_detection':
      return { filePath: 'docs/doc-health-report.md', content: header + convertStaleReport(generatorResult) };

    default:
      console.warn(`[markdownWriter] Unknown doc_type: ${generatorResult.doc_type}`);
      return null;
  }
}

function convertApiReference(result, header) {
  const filename = result.file ? path.basename(result.file, path.extname(result.file)) : 'unknown';
  const filePath = `docs/api/${filename}.md`;

  let md = header;
  md += `# API Reference: \`${result.file || filename}\`\n\n`;
  md += `> ${result.module_description || ''}\n\n`;
  md += `**Language:** ${result.language || 'Unknown'}  \n`;
  md += `**Confidence:** ${result.confidence}  \n`;
  md += `**Last analyzed:** ${result.last_analyzed || new Date().toISOString()}\n\n`;
  md += `---\n\n`;

  if (!result.exports || result.exports.length === 0) {
    md += `_No public exports detected._\n`;
    return { filePath, content: md };
  }

  for (const exp of result.exports) {
    md += `## \`${exp.name}\`\n\n`;

    if (exp.http_method && exp.http_path) {
      md += `**Route:** \`${exp.http_method} ${exp.http_path}\`\n\n`;
    }

    md += `**Type:** ${exp.type}  \n`;
    md += `**Signature:** \`${exp.signature}\`\n\n`;
    md += `${exp.description}\n\n`;

    if (exp.params && exp.params.length > 0) {
      md += `### Parameters\n\n`;
      md += `| Name | Type | Required | Description |\n`;
      md += `|------|------|----------|-------------|\n`;
      for (const p of exp.params) {
        const req = p.required ? 'Yes' : `Optional (default: \`${p.default ?? 'none'}\`)`;
        const constraints = p.constraints ? ` ${p.constraints}` : '';
        md += `| \`${p.name}\` | \`${p.type}\` | ${req} | ${constraints} Example: \`${p.example || ''}\` |\n`;
      }
      md += `\n`;
    }

    if (exp.returns) {
      md += `### Returns\n\n`;
      md += `**Type:** \`${exp.returns.type}\`\n\n`;
      md += `${exp.returns.description}\n\n`;
      if (exp.returns.shape && Object.keys(exp.returns.shape).length > 0) {
        md += `**Shape:**\n\`\`\`json\n${JSON.stringify(exp.returns.shape, null, 2)}\n\`\`\`\n\n`;
      }
    }

    if (exp.throws && exp.throws.length > 0) {
      md += `### Throws\n\n`;
      for (const t of exp.throws) {
        md += `- **\`${t.error}\`** -- ${t.condition}\n`;
      }
      md += `\n`;
    }

    if (exp.example) {
      md += `### Example\n\n\`\`\`javascript\n${exp.example}\n\`\`\`\n\n`;
    }

    if (exp.side_effects && exp.side_effects.length > 0) {
      md += `### Side Effects\n\n`;
      for (const se of exp.side_effects) {
        md += `- ${se}\n`;
      }
      md += `\n`;
    }

    if (exp.notes) {
      md += `> **Note:** ${exp.notes}\n\n`;
    }

    md += `---\n\n`;
  }

  if (result.flags && result.flags.length > 0) {
    md += `## Review Flags\n\n`;
    for (const flag of result.flags) {
      md += `- ${flag}\n`;
    }
  }

  return { filePath, content: md };
}

function convertChangelog(result, header, context) {
  let entry = `## [${result.version_bump?.toUpperCase() || 'PATCH'}] -- PR #${result.pr_number || context.prNumber || '?'} (${result.merged_at || new Date().toISOString().split('T')[0]})\n\n`;

  if (result.breaking) {
    entry += `> **BREAKING CHANGE** -- See migration notes below.\n\n`;
  }

  if (result.entries && result.entries.length > 0) {
    for (const e of result.entries) {
      entry += `### ${e.category}\n\n`;
      entry += `${e.summary}\n\n`;
      if (e.impact) entry += `**Impact:** ${e.impact}\n\n`;
      if (e.migration_note) entry += `**Migration:** ${e.migration_note}\n\n`;
    }
  }

  if (result.flags && result.flags.length > 0) {
    entry += `**Flags:** ${result.flags.join(', ')}\n\n`;
  }

  entry += `---\n\n`;

  return {
    filePath: 'CHANGELOG.md',
    content: entry,
    prepend: true,
  };
}

function convertReadmeUpdate(result) {
  if (!result.needs_update) return null;
  return {
    filePath: 'README.md',
    content: result.updated_readme || '',
  };
}

function convertMigrationGuide(result, header, context) {
  const version = result.to_version || context.version || 'next';
  const filePath = `docs/migrations/v${version}-migration.md`;

  let md = header;
  md += `# Migration Guide: v${result.from_version || 'previous'} -> v${version}\n\n`;
  md += `> ${result.summary || ''}\n\n`;
  md += `**Effort:** ${result.effort}  \n`;
  md += `**Rollback possible:** ${result.rollback_possible ? 'Yes' : 'No'}\n\n`;

  if (result.steps && result.steps.length > 0) {
    md += `## Steps\n\n`;
    for (const step of result.steps) {
      md += `### Step ${step.step}: ${step.title}\n\n`;
      md += `${step.description}\n\n`;
      md += `**Effort:** ${step.effort}${step.irreversible ? ' -- **Irreversible**' : ''}\n\n`;
      if (step.before) {
        md += `**Before:**\n\`\`\`javascript\n${step.before}\n\`\`\`\n\n`;
      }
      if (step.after) {
        md += `**After:**\n\`\`\`javascript\n${step.after}\n\`\`\`\n\n`;
      }
      if (step.files_likely_affected && step.files_likely_affected.length > 0) {
        md += `**Files likely affected:** ${step.files_likely_affected.map((f) => `\`${f}\``).join(', ')}\n\n`;
      }
    }
  }

  if (result.env_vars_changed && result.env_vars_changed.length > 0) {
    md += `## Environment Variable Changes\n\n`;
    for (const ev of result.env_vars_changed) {
      md += `- **${ev.action.toUpperCase()}:** \`${ev.old_name || ''}\` -> \`${ev.new_name || ''}\`\n`;
      md += `  ${ev.instructions}\n\n`;
    }
  }

  if (result.rollback_steps) {
    md += `## Rollback\n\n${result.rollback_steps}\n\n`;
  }

  return { filePath, content: md };
}

function convertArchitecture(result) {
  let md = `# Architecture Overview: ${result.project_name || 'Project'}\n\n`;
  md += `> ${result.one_liner || ''}\n\n`;
  md += `${result.what_it_does || ''}\n\n`;
  md += `**Pattern:** ${result.architecture_pattern || ''}\n\n`;

  if (result.tech_stack && result.tech_stack.length > 0) {
    md += `## Tech Stack\n\n`;
    md += `| Technology | Version | Role | Why |\n`;
    md += `|------------|---------|------|-----|\n`;
    for (const t of result.tech_stack) {
      md += `| ${t.name} | ${t.version || '--'} | ${t.role} | ${t.why} |\n`;
    }
    md += `\n`;
  }

  if (result.critical_path && result.critical_path.length > 0) {
    md += `## Critical Path\n\n`;
    for (const step of result.critical_path) {
      md += `${step}\n`;
    }
    md += `\n`;
  }

  if (result.key_files_to_read_first && result.key_files_to_read_first.length > 0) {
    md += `## Key Files to Read First\n\n`;
    const sorted = [...result.key_files_to_read_first].sort((a, b) => a.read_order - b.read_order);
    for (const f of sorted) {
      md += `${f.read_order}. **\`${f.file}\`** -- ${f.why}\n`;
    }
    md += `\n`;
  }

  if (result.non_obvious_things && result.non_obvious_things.length > 0) {
    md += `## Non-Obvious Things\n\n`;
    for (const thing of result.non_obvious_things) {
      md += `- ${thing}\n`;
    }
    md += `\n`;
  }

  if (result.known_limitations && result.known_limitations.length > 0) {
    md += `## Known Limitations\n\n`;
    for (const lim of result.known_limitations) {
      md += `- ${lim}\n`;
    }
    md += `\n`;
  }

  return md;
}

function convertOnboarding(result) {
  let md = `# Getting Started: ${result.project_name || 'Project'}\n\n`;
  md += `**Estimated setup time:** ${result.estimated_setup_time || '15 minutes'}\n\n`;

  if (result.prerequisites && result.prerequisites.length > 0) {
    md += `## Prerequisites\n\n`;
    for (const p of result.prerequisites) {
      md += `### ${p.tool} ${p.version || ''}\n\n`;
      md += `Check: \`${p.check_command}\`  \n`;
      md += `Install: ${p.install_url}\n`;
      if (p.notes) md += `\n> ${p.notes}\n`;
      md += `\n`;
    }
  }

  if (result.sections && result.sections.length > 0) {
    for (const section of result.sections) {
      md += `## ${section.title}\n\n`;
      if (section.intro) md += `${section.intro}\n\n`;
      if (section.steps) {
        for (const step of section.steps) {
          md += `### ${step.instruction}\n\n`;
          if (step.command) md += `\`\`\`bash\n${step.command}\n\`\`\`\n\n`;
          if (step.expected_output) md += `Expected output:\n\`\`\`\n${step.expected_output}\n\`\`\`\n\n`;
          if (step.pitfall) md += `> **Pitfall:** ${step.pitfall}\n\n`;
        }
      }
      if (section.env_vars) {
        md += `### Environment Variables\n\n`;
        md += `| Variable | Required | Description | Example |\n`;
        md += `|----------|----------|-------------|--------|\n`;
        for (const ev of section.env_vars) {
          md += `| \`${ev.name}\` | ${ev.required ? 'Yes' : 'Optional'} | ${ev.description} | \`${ev.example_value || ''}\` |\n`;
        }
        md += `\n`;
      }
    }
  }

  if (result.verify_success) {
    md += `## Verify It Works\n\n`;
    md += `${result.verify_success.instruction}\n\n`;
    md += `\`\`\`bash\n${result.verify_success.command}\n\`\`\`\n\n`;
    if (result.verify_success.expected_output) {
      md += `Expected:\n\`\`\`\n${result.verify_success.expected_output}\n\`\`\`\n\n`;
    }
  }

  if (result.run_tests) {
    md += `## Run Tests\n\n`;
    md += `\`\`\`bash\n${result.run_tests.command}\n\`\`\`\n\n`;
    if (result.run_tests.expected_output) {
      md += `Expected output:\n\`\`\`\n${result.run_tests.expected_output}\n\`\`\`\n\n`;
    }
  }

  if (result.common_mistakes && result.common_mistakes.length > 0) {
    md += `## Common Mistakes\n\n`;
    for (const m of result.common_mistakes) {
      md += `### ${m.problem}\n\n`;
      md += `**Cause:** ${m.cause}\n\n`;
      md += `**Fix:** ${m.fix}\n\n`;
    }
  }

  return md;
}

function convertRunbook(result) {
  let md = `# Operations Runbook: ${result.service_name || 'Service'}\n\n`;
  md += `> ${result.service_description || ''}\n\n`;
  md += `**Criticality:** ${result.criticality || 'medium'}\n\n`;

  if (result.deployment) {
    md += `## Deployment\n\n`;
    md += `**Platform:** ${result.deployment.platform || '--'}  \n`;
    md += `**Deploy command:** \`${result.deployment.deploy_command || '--'}\`  \n`;
    md += `**Estimated time:** ${result.deployment.deploy_time_estimate || '--'}\n\n`;

    if (result.deployment.verify_steps?.length > 0) {
      md += `### Verify Deployment\n\n`;
      result.deployment.verify_steps.forEach((s, i) => { md += `${i + 1}. ${s}\n`; });
      md += `\n`;
    }

    if (result.deployment.rollback_steps?.length > 0) {
      md += `### Rollback\n\n`;
      result.deployment.rollback_steps.forEach((s, i) => { md += `${i + 1}. ${s}\n`; });
      md += `\n`;
    }
  }

  if (result.monitoring) {
    md += `## Monitoring\n\n`;
    md += `**Health endpoint:** \`${result.monitoring.health_endpoint || '--'}\`\n\n`;
    if (result.monitoring.key_metrics?.length > 0) {
      md += `### Key Metrics\n\n`;
      for (const m of result.monitoring.key_metrics) {
        md += `- **${m.metric}** (${m.where}): Alert at ${m.alert_threshold} -- ${m.meaning}\n`;
      }
      md += `\n`;
    }
  }

  if (result.alerts && result.alerts.length > 0) {
    md += `## Alert Response\n\n`;
    for (const a of result.alerts) {
      md += `### ${a.name}\n\n`;
      md += `**Trigger:** ${a.trigger}  \n`;
      md += `**Likely cause:** ${a.likely_cause}\n\n`;
      md += `#### Remediation\n\n${a.remediation}\n\n`;
    }
  }

  if (result.failure_modes && result.failure_modes.length > 0) {
    md += `## Failure Modes\n\n`;
    for (const fm of result.failure_modes) {
      md += `### ${fm.scenario}\n\n`;
      md += `**Blast radius:** ${fm.blast_radius}\n\n`;
      md += `**Detection:** ${fm.detection}\n\n`;
      md += `**Remediation:** ${fm.remediation}\n\n`;
      md += `**User impact:** ${fm.user_impact}\n\n`;
    }
  }

  if (result.common_tasks && result.common_tasks.length > 0) {
    md += `## Common Tasks\n\n`;
    for (const t of result.common_tasks) {
      md += `### ${t.task}\n\n`;
      md += `\`\`\`bash\n${t.command}\n\`\`\`\n\n`;
      md += `_When to use: ${t.when}_\n\n`;
    }
  }

  return md;
}

function convertDocstrings(result) {
  if (!result.patched_file_content || !result.file) return null;
  return {
    filePath: result.file,
    content: result.patched_file_content,
  };
}

